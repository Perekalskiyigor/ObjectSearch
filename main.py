import cv2
import numpy as np
"""
«Сначала мы превращаем изображение в чёрно-белую маску,
убираем шум, потом ищем центры болтов.
Даже если болты касаются друг друга,
алгоритм находит их середины и аккуратно разделяет объекты,
после чего мы можем корректно посчитать количество деталей.»
"""

# Путь до изображения потом камера будет
IMG = "synthetic_bolts_touch_only_no_overlap.png"  # положи рядом со скриптом

#читает картинку
img = cv2.imread(IMG)
# переводит в градации серого убираем цвет
# Мы выключили «цветную подсветку» и оставили только светло / темно.
gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)

# 1) Бинаризация — «болт / не болт»
# Картинка превращается в чёрно-белую маску
# Болты → белые Фон → чёрный
# Взяли фото и обвели все болты белым маркером, а всё остальное закрасили чёрным.
_, bw = cv2.threshold(gray, 0, 255, cv2.THRESH_BINARY_INV + cv2.THRESH_OTSU)

# Морфология: убрать мусор и «подчистить» силуэты
# Убираем: мелкие точки шум заусенцы по краям
# Как если бы ты слегка стряхнул песок со стола, чтобы остались только болты.
kernel = cv2.getStructuringElement(cv2.MORPH_ELLIPSE, (3,3))
bw = cv2.morphologyEx(bw, cv2.MORPH_OPEN, kernel, iterations=1)

# 2) простой подсчёт (сломается при касаниях) Он берёт бинарную картинку и ищет связные белые области (острова).
# Компьютер ищет белые «острова»
# Каждый остров считается как один болт
# Проблема: Если болты касаются друг друга,
num_labels, _ = cv2.connectedComponents(bw)
cc_count = num_labels - 1

# 3) Watershed для разделения касаний Дальше  разрезать «слипшиеся острова» на отдельные болты.
# Для каждого белого пикселя считается: насколько он далеко от края
# В центре болта:
# значение большое
# На краю болта:
# значение маленькое
# Если идти от края болта к центру — глубина увеличивается.
# Центр болта — самая «глубокая точка».
dist = cv2.distanceTransform(bw, cv2.DIST_L2, 5)
# Выделяем уверенные центры болтов
# Берём только самые глубокие места
# Это почти гарантированно центр каждого болта
# Мы поставили флажок в центр каждого болта,
# даже если они слиплись.
_, sure_fg = cv2.threshold(dist, 0.45 * dist.max(), 255, 0)
sure_fg = sure_fg.astype(np.uint8)

# Выделяем уверенные центры болтов
sure_bg = cv2.dilate(bw, kernel, iterations=2)
unknown = cv2.subtract(sure_bg, sure_fg)

# Каждому центру болта даётся номер
# Зона «непонятно» помечается нулём
# вот болт №1
# вот болт №2
# а вот тут «ничья территория»
_, markers = cv2.connectedComponents(sure_fg)
markers = markers + 1
markers[unknown == 255] = 0

# Watershed — «аккуратно разрезаем»
# Как если бы каждый болт «раздулся» изнутри,
# пока не упёрся в соседний по этой границе режим
markers_ws = cv2.watershed(img.copy(), markers)

# Каждый номер > 1 — это отдельный болт
# Считаем их количество
labels = np.unique(markers_ws)
ws_count = int(np.sum(labels > 1))

# Границы, которые нашёл watershed,
# рисуются красным
# Аналогия:
# Мы маркером обвели, где компьютер разрезал болты.
overlay = img.copy()
overlay[markers_ws == -1] = (0, 0, 255)

print("ConnectedComponents =", cc_count)
print("Watershed           =", ws_count)

